#@save
def sequence_mask(X, valid_len, value=0):
    """在序列中屏蔽不相关的项"""
    maxlen = X.size(1)
    mask = torch.arange((maxlen), dtype=torch.float32,
                        device=X.device).unsqueeze(0) < valid_len.unsqueeze(1)
    X[~mask] = value
    return X

